import torch
import torch.nn as nn
from . import arl, aml


class Module(nn.Module):
    def __init__(
        self,
        n_users: int,
        n_items: int,
        n_factors: int,
        hidden_arl: list,
        hidden_aml: list,
        dropout: float,
        interactions: torch.Tensor, 
    ):
        """
        Deep collaborative recommendation algorithm based on attention mechanism (Cui et al., 2022)
        -----
        Implements the base structure of Deep Collaborative Recommendation Algorithm Based on Attention Mechanism (DACR),
        MF, MLP & history embedding based latent factor model,
        applying attention mechanism to denoise from implicit feedback,
        combining a Attention Representation Learning (aRL) and a Attention Matching Function Learning Networks (aML)
        to learn low-rank linear represenation & high-rank nonlinear user-item interactions.

        Args:
            n_users (int):
                total number of users in the dataset, U.
            n_items (int):
                total number of items in the dataset, I.
            n_factors (int):
                dimensionality of user and item latent representation vectors, K.
            hidden_rl (list):
                layer dimensions for the representation @ RLNet. 
                (e.g., [64, 32, 16, 8])
            hidden_ml (list): 
                layer dimensions for the matching function @ MLNet. 
                (e.g., [64, 32, 16, 8])
            dropout (float): 
                dropout rate applied to MLP layers for regularization.
            interaction (torch.Tensor): 
                user-item interaction matrix, masked evaluation datasets. 
                (shape: [U+1, I+1])
        """
        super().__init__()

        # attr dictionary for load
        self.init_args = locals().copy()
        del self.init_args["self"]
        del self.init_args["__class__"]

        # global attr
        self.n_users = n_users
        self.n_items = n_items
        self.n_factors = n_factors
        self.hidden_arl = hidden_arl
        self.hidden_aml = hidden_aml
        self.dropout = dropout
        self.register_buffer(
            name="interactions", 
            tensor=interactions,
        )

        # generate layers
        self._set_up_components()

    def forward(
        self, 
        user_idx: torch.Tensor, 
        item_idx: torch.Tensor,
    ):
        """
        Training Method

        Args:
            user_idx (torch.Tensor): target user idx (shape: [B,])
            item_idx (torch.Tensor): target item idx (shape: [B,])
        
        Returns:
            logit (torch.Tensor): (u,i) pair interaction logit (shape: [B,])
        """
        return self.score(user_idx, item_idx)

    @torch.no_grad()
    def predict(
        self, 
        user_idx: torch.Tensor, 
        item_idx: torch.Tensor,
    ):
        """
        Evaluation Method

        Args:
            user_idx (torch.Tensor): target user idx (shape: [B,])
            item_idx (torch.Tensor): target item idx (shape: [B,])

        Returns:
            prob (torch.Tensor): (u,i) pair interaction probability (shape: [B,])
        """
        logit = self.score(user_idx, item_idx)
        prob = torch.sigmoid(logit)
        return prob

    def score(self, user_idx, item_idx):
        pred_vector = self.ensemble(user_idx, item_idx)
        logit = self.pred_layer(pred_vector).squeeze(-1)
        return logit

    def ensemble(self, user_idx, item_idx):
        # modules
        pred_vector_arl = self.arl.gmf(user_idx, item_idx)
        pred_vector_aml = self.aml.ncf(user_idx, item_idx)

        # agg
        kwargs = dict(
            tensors=(pred_vector_arl, pred_vector_aml), 
            dim=-1,
        )
        pred_vector = torch.cat(**kwargs)

        return pred_vector

    def _set_up_components(self):
        self._create_modules()
        self._create_layers()

    def _create_modules(self):
        kwargs = dict(
            n_users=self.n_users,
            n_items=self.n_items,
            dropout=self.dropout,
            interactions=self.interactions,
        )
        self.arl = arl.Module(
            **kwargs, 
            hidden=self.hidden_arl,
        )
        self.aml = aml.Module(
            **kwargs, 
            hidden=self.hidden_aml, 
            n_factors=self.n_factors,
        )

    def _create_layers(self):
        kwargs = dict(
            in_features=self.hidden_arl[-1] + self.hidden_aml[-1],
            out_features=1,
        )
        self.pred_layer = nn.Linear(**kwargs)

    def _generate_layers(self, hidden):
        idx = 1
        while idx < len(hidden):
            yield nn.Linear(hidden[idx-1], hidden[idx])
            yield nn.LayerNorm(hidden[idx])
            yield nn.ReLU()
            yield nn.Dropout(self.dropout)
            idx += 1